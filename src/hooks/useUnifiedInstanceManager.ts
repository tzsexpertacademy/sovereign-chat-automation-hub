import { useState, useEffect, useCallback } from 'react';
import whatsappService from '@/services/whatsappMultiClient';
import { whatsappInstancesService } from '@/services/whatsappInstancesService';
import { useToast } from '@/hooks/use-toast';

interface InstanceStatus {
  instanceId: string;
  status: string;
  qrCode?: string;
  hasQrCode?: boolean;
  phoneNumber?: string;
  lastUpdated?: number;
}

interface UseUnifiedInstanceManagerReturn {
  instances: Record<string, InstanceStatus>;
  loading: Record<string, boolean>;
  websocketConnected: boolean;
  connectInstance: (instanceId: string) => Promise<void>;
  disconnectInstance: (instanceId: string) => Promise<void>;
  getInstanceStatus: (instanceId: string) => InstanceStatus;
  isLoading: (instanceId: string) => boolean;
  cleanup: (instanceId: string) => void;
  refreshStatus: (instanceId: string) => Promise<void>;
}

export const useUnifiedInstanceManager = (): UseUnifiedInstanceManagerReturn => {
  const [instances, setInstances] = useState<Record<string, InstanceStatus>>({});
  const [loading, setLoading] = useState<Record<string, boolean>>({});
  const [websocketConnected, setWebsocketConnected] = useState(false);
  const { toast } = useToast();

  // Conectar WebSocket uma √∫nica vez
  useEffect(() => {
    console.log('üîß [UNIFIED] Inicializando Unified Instance Manager');
    
    const socket = whatsappService.connectSocket();
    
    if (socket) {
      const handleConnect = () => {
        console.log('‚úÖ [UNIFIED] WebSocket conectado com sucesso');
        setWebsocketConnected(true);
      };

      const handleDisconnect = (reason: string) => {
        console.log(`‚ùå [UNIFIED] WebSocket desconectado: ${reason}`);
        setWebsocketConnected(false);
        
        // Reconectar automaticamente ap√≥s pequeno delay
        if (reason !== 'io client disconnect') {
          setTimeout(() => {
            console.log('üîÑ [UNIFIED] Tentando reconectar WebSocket...');
            const newSocket = whatsappService.connectSocket();
            if (newSocket) {
              console.log('üîÑ [UNIFIED] Nova conex√£o WebSocket estabelecida');
            }
          }, 2000);
        }
      };

      const handleError = (error: any) => {
        console.error('‚ùå [UNIFIED] Erro WebSocket:', error);
        setWebsocketConnected(false);
      };

      // CONFIGURAR EVENTOS WEBSOCKET
      socket.on('connect', handleConnect);
      socket.on('disconnect', handleDisconnect);
      socket.on('connect_error', handleError);

      // Heartbeat melhorado
      socket.on('ping', () => {
        socket.emit('pong');
        console.log('üíó [UNIFIED] Heartbeat enviado');
      });

      // LOG DE STATUS INICIAL
      if (socket.connected) {
        handleConnect();
      }

      return () => {
        console.log('üßπ [UNIFIED] Limpando listeners WebSocket');
        socket.off('connect', handleConnect);
        socket.off('disconnect', handleDisconnect);
        socket.off('connect_error', handleError);
        socket.off('ping');
      };
    } else {
      console.error('‚ùå [UNIFIED] Falha ao obter socket WebSocket');
    }
  }, []);

  // Fun√ß√£o para normalizar status - SIMPLIFICADA
  const normalizeStatus = useCallback((clientData: any): string => {
    console.log(`üîß [UNIFIED] Normalizando status:`, {
      status: clientData.status,
      phoneNumber: clientData.phoneNumber ? 'Presente' : 'Ausente',
      hasQrCode: clientData.hasQrCode
    });
    
    // REGRA PRINCIPAL: Se tem phoneNumber, est√° conectado
    if (clientData.phoneNumber && clientData.phoneNumber.trim().length > 0) {
      console.log(`‚úÖ [UNIFIED] Status normalizado: phoneNumber presente -> connected`);
      return 'connected';
    }
    
    // USAR STATUS DIRETO DO BACKEND (j√° est√° correto)
    const finalStatus = clientData.status || 'disconnected';
    console.log(`üìä [UNIFIED] Status normalizado: ${clientData.status} -> ${finalStatus}`);
    return finalStatus;
  }, []);

  // Configurar listener para uma inst√¢ncia
  const setupInstanceListener = useCallback((instanceId: string) => {
    console.log(`üéß [UNIFIED] Configurando listener para: ${instanceId}`);
    
    // Limpar listener anterior
    whatsappService.offClientStatus(instanceId);
    
    // Configurar novo listener
    const handleStatusUpdate = (clientData: any) => {
      const normalizedStatus = normalizeStatus(clientData);
      const timestamp = Date.now();
      
      console.log(`üì± [UNIFIED] Status update recebido para ${instanceId}:`, {
        originalStatus: clientData.status,
        normalizedStatus: normalizedStatus,
        phoneNumber: clientData.phoneNumber || 'N/A',
        hasQrCode: clientData.hasQrCode,
        timestamp: clientData.timestamp
      });
      
      // ATUALIZAR ESTADO LOCAL IMEDIATAMENTE
      setInstances(prev => {
        const newState = {
          ...prev,
          [instanceId]: {
            instanceId,
            status: normalizedStatus,
            qrCode: clientData.qrCode,
            hasQrCode: clientData.hasQrCode || false,
            phoneNumber: clientData.phoneNumber,
            lastUpdated: timestamp
          }
        };
        
        console.log(`üîÑ [UNIFIED] Estado atualizado para ${instanceId}:`, newState[instanceId]);
        return newState;
      });

      // SYNC COM BANCO DE DADOS (sem await para n√£o bloquear UI)
      if (normalizedStatus !== 'connecting') {
        const updateData = clientData.phoneNumber ? { phone_number: clientData.phoneNumber } : undefined;
        whatsappInstancesService.updateInstanceStatus(instanceId, normalizedStatus, updateData)
          .then(() => {
            console.log(`‚úÖ [UNIFIED] Banco sincronizado para ${instanceId}: ${normalizedStatus}`);
          })
          .catch((error) => {
            console.error(`‚ùå [UNIFIED] Erro ao sincronizar banco para ${instanceId}:`, error);
          });
      }

      // NOTIFICA√á√ïES DE SUCESSO
      if (normalizedStatus === 'connected' && clientData.phoneNumber) {
        console.log(`üéâ [UNIFIED] WhatsApp conectado com sucesso: ${clientData.phoneNumber}`);
        toast({
          title: "‚úÖ WhatsApp Conectado!",
          description: `Conectado com sucesso: ${clientData.phoneNumber}`,
        });
      } else if (normalizedStatus === 'qr_ready' && clientData.hasQrCode) {
        console.log(`üì± [UNIFIED] QR Code dispon√≠vel para ${instanceId}`);
        toast({
          title: "üì± QR Code Dispon√≠vel!",
          description: "Escaneie o QR Code para conectar",
        });
      }
    };

    whatsappService.onClientStatus(instanceId, handleStatusUpdate);
    whatsappService.joinClientRoom(instanceId);
    
    return handleStatusUpdate;
  }, [normalizeStatus, toast]);

  // Buscar status atual de uma inst√¢ncia
  const refreshStatus = useCallback(async (instanceId: string) => {
    try {
      console.log(`üîÑ [UNIFIED] Buscando status atual: ${instanceId}`);
      const status = await whatsappService.getClientStatus(instanceId);
      const normalizedStatus = normalizeStatus(status);
      
      console.log(`üìä [UNIFIED] Status atual ${instanceId}: ${normalizedStatus}`);
      
      setInstances(prev => ({
        ...prev,
        [instanceId]: {
          instanceId,
          status: normalizedStatus,
          qrCode: status.qrCode,
          hasQrCode: status.hasQrCode || false,
          phoneNumber: status.phoneNumber,
          lastUpdated: Date.now()
        }
      }));
    } catch (error) {
      console.error(`‚ùå [UNIFIED] Erro ao buscar status ${instanceId}:`, error);
      throw error;
    }
  }, [normalizeStatus]);

  // Conectar inst√¢ncia
  const connectInstance = useCallback(async (instanceId: string) => {
    try {
      setLoading(prev => ({ ...prev, [instanceId]: true }));
      console.log(`üöÄ [UNIFIED] Conectando inst√¢ncia: ${instanceId}`);
      
      // Configurar listener antes de conectar
      setupInstanceListener(instanceId);
      
      // Aguardar configura√ß√£o
      await new Promise(resolve => setTimeout(resolve, 1000));
      
      // Iniciar conex√£o
      await whatsappService.connectClient(instanceId);
      
      // Polling inteligente como backup - REDUZIDO
      let pollCount = 0;
      const maxPolls = 10; // Reduzido de 20 para 10
      
      const pollInterval = setInterval(async () => {
        pollCount++;
        console.log(`üîÑ [UNIFIED] Polling ${instanceId} (${pollCount}/${maxPolls})`);
        
        try {
          await refreshStatus(instanceId);
          const currentStatus = getInstanceStatus(instanceId);
          
          // PARAR POLLING SE QR CODE APARECER OU CONECTAR
          if (currentStatus.hasQrCode || currentStatus.status === 'connected') {
            console.log(`‚úÖ [UNIFIED] Polling finalizado para ${instanceId}: ${currentStatus.status}`);
            clearInterval(pollInterval);
          } else if (pollCount >= maxPolls) {
            console.log(`‚è∞ [UNIFIED] Polling timeout para ${instanceId} ap√≥s ${maxPolls} tentativas`);
            clearInterval(pollInterval);
          }
        } catch (error) {
          console.warn(`‚ö†Ô∏è [UNIFIED] Erro no polling ${pollCount}:`, error);
          if (pollCount >= maxPolls) {
            clearInterval(pollInterval);
          }
        }
      }, 4000); // Aumentado de 3000 para 4000ms (menos agressivo)
      
      toast({
        title: "Conectando...",
        description: "Aguarde o QR Code aparecer",
      });
      
    } catch (error: any) {
      console.error('‚ùå [UNIFIED] Erro ao conectar:', error);
      toast({
        title: "Erro na Conex√£o",
        description: error.message,
        variant: "destructive",
      });
      throw error;
    } finally {
      setLoading(prev => ({ ...prev, [instanceId]: false }));
    }
  }, [setupInstanceListener, refreshStatus, toast]);

  // Desconectar inst√¢ncia
  const disconnectInstance = useCallback(async (instanceId: string) => {
    try {
      setLoading(prev => ({ ...prev, [instanceId]: true }));
      console.log(`üîå [UNIFIED] Desconectando inst√¢ncia: ${instanceId}`);
      
      await whatsappService.disconnectClient(instanceId);
      whatsappService.offClientStatus(instanceId);
      
      setInstances(prev => ({
        ...prev,
        [instanceId]: {
          ...prev[instanceId],
          status: 'disconnected',
          qrCode: undefined,
          hasQrCode: false
        }
      }));

      await whatsappInstancesService.updateInstanceStatus(instanceId, 'disconnected');

      toast({
        title: "Desconectado",
        description: "Inst√¢ncia desconectada com sucesso",
      });
      
    } catch (error: any) {
      console.error('‚ùå [UNIFIED] Erro ao desconectar:', error);
      toast({
        title: "Erro",
        description: error.message,
        variant: "destructive",
      });
      throw error;
    } finally {
      setLoading(prev => ({ ...prev, [instanceId]: false }));
    }
  }, [toast]);

  // Obter status de uma inst√¢ncia
  const getInstanceStatus = useCallback((instanceId: string): InstanceStatus => {
    return instances[instanceId] || { instanceId, status: 'disconnected' };
  }, [instances]);

  // Verificar se est√° carregando
  const isLoading = useCallback((instanceId: string): boolean => {
    return loading[instanceId] || false;
  }, [loading]);

  // Limpar inst√¢ncia
  const cleanup = useCallback((instanceId: string) => {
    whatsappService.offClientStatus(instanceId);
    setInstances(prev => {
      const newInstances = { ...prev };
      delete newInstances[instanceId];
      return newInstances;
    });
  }, []);

  return {
    instances,
    loading,
    websocketConnected,
    connectInstance,
    disconnectInstance,
    getInstanceStatus,
    isLoading,
    cleanup,
    refreshStatus
  };
};